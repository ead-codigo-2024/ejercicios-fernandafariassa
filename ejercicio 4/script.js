document.getElementById("contactForm").addEventListener("submit", function(event) {
    event.preventDefault();
    
    let name = document.getElementById("name");
    let email = document.getElementById("email");
    let phone = document.getElementById("phone");
    let message = document.getElementById("message");
    let errorMessage = document.getElementById("errorMessage");

    let isValid = true;

    // Función para validar y actualizar los bordes
    function validateField(field) {
        if (!field.value.trim()) {  // Verifica que no esté vacío ni tenga solo espacios
            field.classList.add("invalid");
            field.classList.remove("valid");
            isValid = false;
        } else {
            field.classList.add("valid");
            field.classList.remove("invalid");
        }
    }

    // Validamos cada campo
    validateField(name);
    validateField(email);
    validateField(phone);
    validateField(message);

    if (!isValid) {
        errorMessage.textContent = "Por favor, llena todos los campos correctamente.";
    } else {
        errorMessage.textContent = "";
        alert("Formulario enviado con éxito!");
        document.getElementById("contactForm").reset();

        // Restablecemos los bordes al color original después de enviar
        name.classList.remove("valid", "invalid");
        email.classList.remove("valid", "invalid");
        phone.classList.remove("valid", "invalid");
        message.classList.remove("valid", "invalid");
    }
});
